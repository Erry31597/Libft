#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if(n < 0)
	{
		ft_putchar_fd(fd, "-", 1),
		n = -n;
	}
	if (n >= 9)
	{
		ft_putnbr_fd(n, fd);
		n = n % 10;
	}
	ft_putchar_fd((n % 10) + '0');
}